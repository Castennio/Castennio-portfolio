export default function QuestionCard({
  option,
  isSelected,
  onSelect,
  index,
}: QuestionCardProps) {
  const cardRef = useRef<HTMLButtonElement>(null);
  const checkRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (checkRef.current) {
      gsap.to(checkRef.current, {
        scale: isSelected ? 1 : 0,
        opacity: isSelected ? 1 : 0,
        duration: 0.3,
        ease: "back.out(1.7)",
      });
    }
  }, [isSelected]);

  return (
    <button
      ref={cardRef}
      onClick={onSelect}
      className={`option-card relative w-full p-5 rounded-xl border text-left transition-all duration-300 ${
        isSelected
          ? "bg-blue-500/[0.1] border-blue-500/35"
          : "bg-[#14151a] border-white/[0.08] hover:bg-[#1a1b22] hover:border-white/[0.15]"
      }`}
      style={{ animationDelay: `${index * 0.05}s` }}
    >
      <div className="flex items-start gap-4">
        <div
          className={`w-10 h-10 rounded-lg flex items-center justify-center shrink-0 transition-colors duration-300 ${
            isSelected
              ? "bg-blue-500/20 text-blue-400"
              : "bg-white/[0.04] text-white/40"
          }`}
        >
          {icons[option.icon] || icons.sparkles}
        </div>
        <div className="flex-1 min-w-0">
          <p
            className={`font-medium transition-colors duration-300 ${
              isSelected ? "text-white" : "text-white/80"
            }`}
          >
            {option.label}
          </p>
          <p className="text-[13px] text-white/40 mt-0.5">{option.description}</p>
        </div>
        <div
          ref={checkRef}
          className="w-6 h-6 rounded-full bg-blue-500 flex items-center justify-center shrink-0"
          style={{ transform: "scale(0)", opacity: 0 }}
        >
          <svg className="w-3.5 h-3.5 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={3} d="M5 13l4 4L19 7" />
          </svg>
        </div>
      </div>
    </button>
  );
}
